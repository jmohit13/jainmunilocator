					</li><br /><li>
						<h3>Victory of Sense of Sight (चक्षु-इन्द्रिय-विजय)</h3>
						To leave completely any sense of Joy or sorrow by seeing beautiful or ugly things or scenes.
					</li><br /><li>
						<h3>Victory of Sense of Hearing (कर्ण-इन्द्रिय-विजय)</h3>

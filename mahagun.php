					</li><br /><li>
						<h3>Victory of Sense of Taste (रसना-इन्द्रिय-विजय)</h3>
						Not to have liking or disliking for things having different taste like sweet, Sour, salty, spicy  etc.
					</li><br /><li>
						<h3>Victory of Sense of Smell (घ्राण-इन्द्रिय-विजय)</h3>
